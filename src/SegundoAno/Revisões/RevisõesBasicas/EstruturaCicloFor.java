package SegundoAno.Revisões.RevisõesBasicas;

public class EstruturaCicloFor {
    public static void main(String[] args) {
        
        //for (inicio; fim; incremento)(bloco a executar)
        for(int i =1; i<=10;i=i+2){
        System.out.println(i);
        }
    //####################################################
        //outro exemplo... ao contrario 
        // for (inicio; fim; decremento)(bloco a executar)
        
        System.out.println("// outro exemplo...");
        for(int count=10; count >=1; count --){
            System.out.println(count);
        }
    }
    
}
