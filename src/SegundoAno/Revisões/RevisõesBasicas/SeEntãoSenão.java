package SegundoAno.Revisões.RevisõesBasicas;

public class SeEntãoSenão {
    public static void main(String[] args) {
        if(true){
            //se for verdade
            System.out.println("Executou o true!");
        }else{
            //se for falso
            System.out.println("executou o false");
        }
        
        //exemplo mais completo com condição em parentesis
        byte idade = 18;
        if(idade >=18){
            //se verdadeiro
            System.out.println("Utilizador maior de idade!");
        }else{
            //se for falso
            System.out.println("Utilizador menor de idade");
        }
    }
    
}
